def run_quiz():
    # Определяем вопросы и ответы
    questions = [
        {
            "question": "1. Какой протокол обеспечивает надёжную передачу данных и контроль порядка?",
            "options": ["UDP", "IP", "TCP", "FTP"],
            "correct_answer": "TCP"
        },
        {
            "question": "2. Что делает протокол ARP?",
            "options": [
                "Шифрует пакеты данных",
                "Передаёт HTML-документы", 
                "Находит MAC-адрес по IP-адресу",
                "Запрашивает имя пользователя"
            ],
            "correct_answer": "Находит MAC-адрес по IP-адресу"
        },
        {
            "question": "3. Какой из перечисленных протоколов используется для передачи электронной почты?",
            "options": ["SMTP", "HTTPS", "SSH", "FTP"],
            "correct_answer": "SMTP"
        }
    ]
    
    score = 0
    
    print("Добро пожаловать в викторину по сетевым протоколам!")
    print("=" * 50)
    
    # Проходим по всем вопросам
    for i, q in enumerate(questions, 1):
        print(f"\n{q['question']}")
        print("-" * 30)
        
        # Выводим варианты ответов
        for j, option in enumerate(q['options'], 1):
            print(f"{j}. {option}")
        
        # Получаем ответ пользователя
        while True:
            try:
                user_answer = input("\nВведите номер правильного ответа (1-4): ") #индекс совпал с цифрой - номером ключа  , которую набрал ти, проходящий викторину
                answer_index = int(user_answer) - 1
                
                if 0 <= answer_index < len(q['options']): #проверка , если вдруг , номер индекса (номер ключа в паре 1: значение) ответов не 1 не 2 ни 3 ни 4 , а вообще больше чем надо или мненьше 0
                    break
                else:
                    print("Пожалуйста, введите число от 1 до 4")
            except ValueError:
                print("Пожалуйста, введите корректное число") #подсказывам , если вдруг введёная цифра не в списке всех ключей 
        
        # Проверяем ответ
        user_choice = q['options'][answer_index]
        if user_choice == q['correct_answer']: #совпал ответ , проверка индекса (порядкового номера оветов)
            print("Правильно!")
            score += 1 #счёт правильных ответов прибавляется, если ответ выбран правильный
        else:
            print(f"Неправильно. Правильный ответ: {q['correct_answer']}")
    
    # Выводим результаты
    print("\n" + "=" * 50)
    print("Викторина завершена!")
    print(f"{formatted_name}, Ваш результат: {score} из {len(questions)}")
    
    # Оценка результатов
    if score == len(questions):
        print(f"Отлично! Вы знаток сетевых протоколов!")
    elif score >= len(questions) // 2:
        print(f"Хороший результат!")
    else:
        print(f"Советуем повторить сетевые протоколы!")

# Запускаем викторину
if __name__ == "__main__":
    run_quiz()